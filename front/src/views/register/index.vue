<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="registerForm" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Login Form</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="commonName"
          v-model="registerForm.commonName"
          placeholder="CommonName"
          name="commonName"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          ref="password"
          v-model="registerForm.password"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          show-password
        />
      </el-form-item>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="registerForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="3"
          auto-complete="on"
        />
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleRegister">注册</el-button>

    </el-form>
  </div>
</template>

<script>
import axios from 'axios'
import qs from 'qs'

export default {
  name: 'Index',
  data() {
    return {
      registerForm: {
        commonName: '',
        password: '',
        username: ''
      },
      id: ''
    }
  },
  methods: {
    handleRegister() {
      axios.post('/commenUser/post',
        {
          commenUserName: this.registerForm.commonName,
          password: this.registerForm.password,
          username: this.registerForm.username
        }).then(res => {
        this.id = res.data.id
        this.$store.commit('user/SET_USERNAME', res.data.commenUserName)
      })
      axios.get('/commenUser/reflash',
        qs.stringify({
          id: this.id
        }))
      this.$router.push('/login')
    }
  }
}
</script>

